package Type_Casting;

public class Data_Types {

	public static void main(String[] args) {

		System.out.println("This is 'Byte' Data type info");
		System.out.println("Size of Byte is = " + Byte.SIZE);
		System.out.println(Byte.MIN_VALUE);
		System.out.println(Byte.MAX_VALUE + "\n");

		System.out.println("This is 'Short' Data type info");
		System.out.println("Size of short is = " + Short.SIZE);
		System.out.println(Short.MIN_VALUE);
		System.out.println(Short.MAX_VALUE + "\n");

		System.out.println("This is Integer Data type info");
		System.out.println("Size of Int is = " + Integer.SIZE);
		System.out.println(Integer.MIN_VALUE);
		System.out.println(Integer.MAX_VALUE + "\n");

		System.out.println("This is Long Data type info");
		System.out.println("Size of Long is = " + Long.SIZE);
		System.out.println(Long.MIN_VALUE);
		System.out.println(Long.MAX_VALUE + "\n");

	}

}
